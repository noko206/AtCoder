using mint = modint1000000007;

int main() {
    int n, p;
    cin >> n >> p;
    vector<mint> a(n);
    REP(i, n) {
        ll tmp;
        cin >> tmp;
        a[i] = tmp;
    }
    if (p == 0) {
        ll ans = 0;
        int cnt = 0;
        REP(i, n) {
            if (a[i].val() == 0) {
                ans += i;
                ++cnt;
            } else {
                ans += cnt;
            }
        }
        output(ans);
        return 0;
    }
    unordered_map<int, int> mp;
    ll ans = 0;
    REP(i, n) {
        if (a[i].val() == 0) continue;
        mint tmp = p / a[i];
        if (mp.find(tmp.val()) != mp.end()) {
            ans += mp[tmp.val()];
        }
        ++mp[a[i].val()];
    }
    output(ans);
}
