// clang-format on
int main() {
    int n, m, l, s, t;
    cin >> n >> m >> l >> s >> t;
    vector<vector<pair<int, int>>> to(n);
    REP(i, m) {
        int u, v, c;
        cin >> u >> v >> c;
        --u, --v;
        to[u].emplace_back(v, c);
    }
    vector<bool> ok(n, false);
    auto dfs = [&](auto& dfs, int v, ll sum = 0, int cnt = 0) -> void {
        if (cnt == l) {
            if (s <= sum && sum <= t) {
                ok[v] = true;
            }
            return;
        }
        for (auto [u, c] : to[v]) {
            dfs(dfs, u, sum + c, cnt + 1);
        }
    };
    dfs(dfs, 0);
    vector<int> ans;
    REP(i, n) {
        if (ok[i]) ans.push_back(i + 1);
    }
    output(ans);
}
